  /**
   * Returns the holiday name for the given YYYY-MM-DD string,
   * or null if the date is not a holiday.
   */
  static get(dateStr) {
    if (this.#LUNAR[dateStr]) return this.#LUNAR[dateStr];
    const mmdd = dateStr.slice(5); // "MM-DD"
    return this.#SOLAR[mmdd] || null;
  }

  /** True if the date string falls on a holiday. */
  static isHoliday(dateStr) {
    return !!this.get(dateStr);
  }

  /**
   * Returns an object with pre-computed flags for a date string:
   * { holiday: string|null, isHoliday: bool, isSunday: bool, isSaturday: bool, isRed: bool }
   */
  static info(dateStr) {
    const d         = new Date(dateStr + 'T00:00:00');
    const dow       = d.getDay();
    const holiday   = this.get(dateStr);
    const isHoliday = !!holiday;
    const isSunday  = dow === 0;
    const isSat     = dow === 6;
    return {
      holiday,
      isHoliday,
      isSunday,
      isSaturday: isSat,
      isRed:      isHoliday || isSunday,
    };
  }
